package com.dt5000.ischool.entity;

/**
 * 课程表实体类
 * 
 * @author 周锋
 * @date 2016年1月21日 下午3:30:24
 * @ClassInfo com.dt5000.ischool.entity.LessonTable
 * @Description
 */
public class LessonTable {

	private String mon;// 星期一
	private String tue;// 星期二
	private String wed;// 星期三
	private String thur;// 星期四
	private String fri;// 星期五
	private String sat;// 星期六
	private String sun;// 星期日

	public String getMon() {
		return mon;
	}

	public void setMon(String mon) {
		this.mon = mon;
	}

	public String getTue() {
		return tue;
	}

	public void setTue(String tue) {
		this.tue = tue;
	}

	public String getWed() {
		return wed;
	}

	public void setWed(String wed) {
		this.wed = wed;
	}

	public String getThur() {
		return thur;
	}

	public void setThur(String thur) {
		this.thur = thur;
	}

	public String getFri() {
		return fri;
	}

	public void setFri(String fri) {
		this.fri = fri;
	}

	public String getSat() {
		return sat;
	}

	public void setSat(String sat) {
		this.sat = sat;
	}

	public String getSun() {
		return sun;
	}

	public void setSun(String sun) {
		this.sun = sun;
	}

}
